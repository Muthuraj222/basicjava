class Dowhile {
    public static void main(String args[])
    {
        int i=5;
        // dowhile normal while if the condition is true then only while loop executed 
        // but if the condition is false we use dowhile to execute the else part
        //atleast once we execute the output in do while if the condition is false
        do
        {
            System.out.println("Monday");
            i++;
        }while(i<=4);
    }
    
}
