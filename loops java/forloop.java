class Forloop 
{
    public static void main(String args[])
    {
        for(int i=0;i<=6;i++)
        {
            for(int j=0;j<=9;j++)
            {
                System.out.println("  " + (j+9) + " - " +(j+10) + "am");
            }
            System.out.println("Day" + i);
        }
    }
    
}
